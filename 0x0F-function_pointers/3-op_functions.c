/**
 * op_add - adds two numbers together
 * @a: first number
 * @b: second number
 * Return: result of operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers together
 * @a: first number
 * @b: second number
 * Return: result of operation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gives the remendir if we divide a by b
 * @a: first number
 * @b: second number
 * Return: result of operation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
